class SecurityManager:
    """Rate limiting –∏ –∑–∞—â–∏—Ç–∞ –æ—Ç —Å–ø–∞–º–∞"""
    
    def __init__(self, max_requests: int = 30, time_window: int = 60):
        """Args: max_requests - –º–∞–∫—Å–∏–º—É–º –∑–∞–ø—Ä–æ—Å–æ–≤, time_window - –≤—Ä–µ–º–µ–Ω–Ω–æ–µ –æ–∫–Ω–æ"""
        self.max_requests = max_requests
        self.time_window = time_window
        self.user_requests: Dict[int, List[float]] = {}
        logger.info(f"üîê SecurityManager: {max_requests} –∑–∞–ø—Ä–æ—Å–æ–≤ –≤ {time_window}—Å")
    
    async def check_rate_limit(self, user_id: int) -> dict:
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç rate limit –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
        now = time.time()
        
        if user_id not in self.user_requests:
            self.user_requests[user_id] = []
        
        # –£–¥–∞–ª—è–µ–º —Å—Ç–∞—Ä—ã–µ –∑–∞–ø—Ä–æ—Å—ã
        self.user_requests[user_id] = [
            ts for ts in self.user_requests[user_id]
            if now - ts < self.time_window
        ]
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º –ª–∏–º–∏—Ç
        if len(self.user_requests[user_id]) >= self.max_requests:
            oldest = self.user_requests[user_id][0]
            retry_in = int(self.time_window - (now - oldest)) + 1
            
            logger.warning(
                f"‚ö†Ô∏è Rate limit –¥–ª—è {user_id}: "
                f"{len(self.user_requests[user_id])}/{self.max_requests}"
            )
            
            return {
                "allowed": False,
                "message": f"‚ö†Ô∏è –°–ª–∏—à–∫–æ–º –º–Ω–æ–≥–æ –∑–∞–ø—Ä–æ—Å–æ–≤. "
                          f"–ü–æ–ø—Ä–æ–±—É–π —á–µ—Ä–µ–∑ {retry_in} —Å–µ–∫—É–Ω–¥",
                "retry_in": retry_in
            }
        
        # –î–æ–±–∞–≤–ª—è–µ–º —Ç–µ–∫—É—â–∏–π –∑–∞–ø—Ä–æ—Å
        self.user_requests[user_id].append(now)
        
        return {
            "allowed": True,
            "message": None,
            "retry_in": 0
        }
    
    def get_user_requests_count(self, user_id: int) -> int:
        """–ü–æ–ª—É—á–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ç–µ–∫—É—â–∏—Ö –∑–∞–ø—Ä–æ—Å–æ–≤ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
        now = time.time()
        
        if user_id not in self.user_requests:
            return 0
        
        active = [
            ts for ts in self.user_requests[user_id]
            if now - ts < self.time_window
        ]
        
        return len(active)
    
    def reset_user(self, user_id: int):
        """–°–±—Ä–∞—Å—ã–≤–∞–µ—Ç —Å—á–µ—Ç—á–∏–∫ –∑–∞–ø—Ä–æ—Å–æ–≤ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
        if user_id in self.user_requests:
            self.user_requests[user_id] = []
            logger.info(f"üîÑ Rate limit —Å–±—Ä–æ—à–µ–Ω –¥–ª—è {user_id}")


