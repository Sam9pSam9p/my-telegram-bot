class AddressValidator:
    """–í–∞–ª–∏–¥–∞—Ü–∏—è –∞–¥—Ä–µ—Å–æ–≤ —Ä–∞–∑–ª–∏—á–Ω—ã—Ö –±–ª–æ–∫—á–µ–π–Ω–æ–≤"""
    
    PATTERNS = {
        "evm": r'^0x[a-fA-F0-9]{40}$',
        "solana": r'^[1-9A-HJ-NP-Za-km-z]{32,44}$',
    }
    
    @staticmethod
    def validate(address: str, chain: str = "auto") -> dict:
        """–í–∞–ª–∏–¥–∏—Ä—É–µ—Ç –∞–¥—Ä–µ—Å –±–ª–æ–∫—á–µ–π–Ω–∞"""
        address = address.strip()
        
        if not address:
            return {
                "valid": False,
                "error": "‚ùå –ê–¥—Ä–µ—Å –ø—É—Å—Ç",
                "chain": None,
                "normalized": None
            }
        
        # EVM –∞–¥—Ä–µ—Å–∞ (Ethereum, Base, BSC)
        if address.startswith("0x"):
            if not re.match(AddressValidator.PATTERNS["evm"], address):
                logger.warning(f"‚ùå –ù–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç EVM –∞–¥—Ä–µ—Å–∞: {address[:10]}...")
                return {
                    "valid": False,
                    "error": "‚ùå –ù–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç EVM –∞–¥—Ä–µ—Å–∞\n"
                             "–î–æ–ª–∂–µ–Ω –±—ã—Ç—å: 0x + 40 hex —Å–∏–º–≤–æ–ª–æ–≤",
                    "chain": None,
                    "normalized": None
                }
            
            logger.info(f"‚úÖ EVM –∞–¥—Ä–µ—Å –≤–∞–ª–∏–¥–µ–Ω: {address[:10]}...")
            return {
                "valid": True,
                "error": None,
                "chain": "evm",
                "normalized": address.lower()
            }
        
        # Solana –∞–¥—Ä–µ—Å–∞
        if re.match(AddressValidator.PATTERNS["solana"], address):
            logger.info(f"‚úÖ Solana –∞–¥—Ä–µ—Å –≤–∞–ª–∏–¥–µ–Ω: {address[:10]}...")
            return {
                "valid": True,
                "error": None,
                "chain": "solana",
                "normalized": address
            }
        
        logger.warning(f"‚ùå –ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç –∞–¥—Ä–µ—Å–∞: {address[:10]}...")
        return {
            "valid": False,
            "error": "‚ùå –ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç –∞–¥—Ä–µ—Å–∞\n\n"
                     "–ü–æ–¥–¥–µ—Ä–∂–∏–≤–∞—é:\n"
                     "‚Ä¢ EVM (0x...)\n"
                     "‚Ä¢ Solana (...)",
            "chain": None,
            "normalized": None
        }
    
    @staticmethod
    def is_evm(address: str) -> bool:
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç —á—Ç–æ —ç—Ç–æ EVM –∞–¥—Ä–µ—Å"""
        return bool(re.match(AddressValidator.PATTERNS["evm"], address))
    
    @staticmethod
    def is_solana(address: str) -> bool:
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç —á—Ç–æ —ç—Ç–æ Solana –∞–¥—Ä–µ—Å"""
        return bool(re.match(AddressValidator.PATTERNS["solana"], address))


